package AllJavaProgram;

public class ForLoopEx {

	public static void main(String[] args) {
		
		for(int i=1;i<100;i++)
		{
			
			
			System.out.println(i);
			
			if(i%2==0)
			{
				System.out.println("Even Number");
			}
			
			else
				System.out.println("Odd number");
			
			
			
		}
		

	}

}
