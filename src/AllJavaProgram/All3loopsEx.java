package AllJavaProgram;

public class All3loopsEx {

	public static void main(String[] args) {
		
		int output=0;
		
		for(int i=0; i<=100; i++)
		{
			
			output=output+i; 
			System.out.println(output);
			
		}
		
	System.out.println("**************************");
		System.out.println(output);
	}

}
